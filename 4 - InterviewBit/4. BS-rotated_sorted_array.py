def rotated_sorted_array(A, B):
    n = len(A)
    lo, hi, mid = 0, n-1, n//2
    
    if A[hi] > A[lo]:
        pivot = 0
    else:
        while lo < hi:
            
            if A[mid] > A[mid+1]:
                pivot = mid+1
                break
            
            if A[mid-1] > A[mid]:
                pivot = mid
                break
            
            if A[lo] > A[mid]:
                hi = mid
                mid //= 2
                
            if A[mid] > A[hi]:
                lo = mid
                mid += (hi-lo)//2
                
    if pivot:
        i = 0
    else:
        i = 1
    
    while i < 2:
        
        if i:
            lo, hi, mid = pivot, n-1, pivot + ((n-pivot)//2)
        else:
            lo, hi, mid = 0, pivot-1, pivot//2
            
        while lo +1 < hi:
            
            if A[mid] == B: return mid
            if A[lo] == B: return lo
            if A[hi] == B: return hi
            
            if A[mid] > B:
                hi = mid
                mid //= 2
                
            else:
                lo = mid
                mid += (hi-lo)//2
        i += 1
    return -1


