def search_range(A, B):
    if not A:
        return [-1, -1]
    if len(A) == 1 and A[0] == B:
        return [0, 0]
    if len(A) == 1 and A[0] != B:
        return [-1, -1]
        
    i, res = 0, []
    if A[0] == B:
        res.append(0)
        i += 1
    
    while i < 2:
        lo, hi, mid = 0, len(A)-1, len(A)//2
        while lo < hi:
#            print(i, mid, '===',A[mid-1], A[mid], A[mid+1],'===',lo,hi, res)
            
            if i and A[-1] == B:
                res.append(len(A)-1)
                break
            
            if i or A[mid] <= B:
                if A[mid] == B and A[mid+1] != B:
                    res.append(mid)
                    break
                
                if A[mid] <= B: # offender !!!
                    lo = mid
                    mid += (hi-lo)//2
                    
                else:
                    hi = mid
                    mid = mid//2
            
            if not i and A[mid] >= B:
                if A[mid-1] != B and A[mid] == B:
                    res.append(mid)
                    break
                
                if A[mid] >= B:
                    hi = mid
                    mid = mid//2
                    
                else:
                    lo = mid
                    mid += (hi-lo)//2
            
            # case where B COULD be in the middle, BUT it's NOT.
            elif A[mid-1] < B < A[mid] or A[mid] < B < A[mid+1]:
                break
            
        i += 1
        
    if not res:
        return [-1, -1]
    if res[0] > res[1]:
        res[0], res[1] = res[1], res[0]
    return res
    
