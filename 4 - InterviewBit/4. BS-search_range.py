    while left <= right:
        mid = (left + right) // 2
        if A[mid] == B and (mid == 0 or A[mid - 1] != B):
            start = mid
            break
        if A[mid] < B:
            left = mid + 1
        else:
            right = mid - 1
            
    left = 0
    right = len(A) - 1
    while left <= right:
        mid = (left + right) // 2
        if A[mid] == B and (mid == len(A) - 1 or A[mid + 1] != B):
            end = mid
            break
        if A[mid] <= B:
            left = mid + 1
        else:
            right = mid - 1
            
    return [start, end]

